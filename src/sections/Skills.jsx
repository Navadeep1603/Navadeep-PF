function SkillCard({ skill }) {
    const cardRef = useRef(null);

    const handleMouseMove = (e) => {
        const card = cardRef.current;
        if (!card) return;
        const rect = card.getBoundingClientRect();
        const x = e.clientX - rect.left;
        const y = e.clientY - rect.top;
        const centerX = rect.width / 2;
        const centerY = rect.height / 2;
        const rotateX = ((y - centerY) / centerY) * -10;
        const rotateY = ((x - centerX) / centerX) * 10;
        card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateZ(10px)`;
    };

    const handleMouseLeave = () => {
        if (cardRef.current) {
            cardRef.current.style.transform = 'perspective(1000px) rotateX(0) rotateY(0) translateZ(0)';
        }
    };

    return (
        <div
            ref={cardRef}
            className="glass tilt-card"
            onMouseMove={handleMouseMove}
            onMouseLeave={handleMouseLeave}
            style={{
                padding: '36px 24px',
                textAlign: 'center',
                cursor: 'default',
                transition: 'transform 0.15s ease, box-shadow 0.3s',
                position: 'relative',
                overflow: 'hidden',
            }}
            onMouseEnter={e => {
                e.currentTarget.style.boxShadow = `0 0 30px ${skill.color}22, 0 0 60px ${skill.color}11, inset 0 0 30px ${skill.color}08`;
                e.currentTarget.style.borderColor = `${skill.color}44`;
            }}
            onMouseOut={e => {
                e.currentTarget.style.boxShadow = 'none';
                e.currentTarget.style.borderColor = 'rgba(255,255,255,0.08)';
            }}
        >
            {/* Glow ring */}
            <div
                style={{
                    width: 72,
                    height: 72,
                    borderRadius: '50%',
                    border: `2px solid ${skill.color}33`,
                    display: 'flex',
                    alignItems: 'center',
                    justifyContent: 'center',
                    margin: '0 auto 16px',
                    color: skill.color,
                    transition: 'all 0.3s',
                }}
            >
                {skill.icon}
            </div>
            <h3
                style={{
                    fontFamily: '"Orbitron", sans-serif',
                    fontSize: '0.85rem',
                    fontWeight: 600,
                    color: 'rgba(255,255,255,0.8)',
                    letterSpacing: 1,
                }}
            >
                {skill.name}
            </h3>
        </div>
    );
}

